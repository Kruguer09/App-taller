    private void printPDF() {
        String invoceName = "factura_A0" + invoice.getNumFactura() + "-" + invoice.getCurrentDate().substring(6) + "_" + invoice.getNifClient() + "_" 
                + invoice.getCurrentDate().substring(0, 2) + invoice.getCurrentDate().substring(3, 5) + invoice.getCurrentDate().substring(6) + ".pdf";
        try {
            PdfDocument pdf = new PdfDocument(new PdfWriter(path + invoceName));
            Document doc = new Document(pdf);

            //Agrega rótulo
            Table header = new Table(new float[]{1});
            header.setWidthPercent(100);
            header.addCell("FACTURA").setBold().setTextAlignment(TextAlignment.CENTER);
            doc.add(header);
            //Añadimos salto de línea
            doc.add(new Paragraph("\n"));

            //Agrega imagen
            Image img = new Image(ImageDataFactory.create(getClass().getResource(imgPath)));
            img.setHeight(100);
            img.setWidth(200);
            doc.add(img);
            //Añadimos salto de línea
            doc.add(new Paragraph("\n"));

            //Primera línea y siguientes
            doc.add(new Paragraph("Información del cliente").setBold());
            doc.add(new Paragraph("Nº FACTURA: " + "A0" + invoice.getNumFactura() + "-" + invoice.getCurrentDate().substring(6)));
            doc.add(new Paragraph("Fecha: " + invoice.getCurrentDate()));
            doc.add(new Paragraph("NIF Cliente: " + invoice.getNifClient()));
            //Datos de la compañía
            doc.add(new Paragraph("NEUMÁTICOS NEUFASHION").setBold().setTextAlignment(TextAlignment.RIGHT));
            doc.add(new Paragraph("Calle locomotor 40").setTextAlignment(TextAlignment.RIGHT));
            doc.add(new Paragraph("14089 Córdoba").setTextAlignment(TextAlignment.RIGHT));
            doc.add(new Paragraph(invoice.getNifCompany()).setTextAlignment(TextAlignment.RIGHT));
            //Añadimos salto de línea
            doc.add(new Paragraph("\n"));

            //Contruimos la tabla de datos
            Table data = new Table(new float[]{2, 2, 2});
            data.setWidthPercent(100);
            //Cabeceras
            data.addHeaderCell(new Cell().add("Unidades"));
            data.addHeaderCell(new Cell().add("Concepto"));
            data.addHeaderCell(new Cell().add("Precio"));

            //Sacamos los datos de las líneas de factura
            linesList = con.readTableInvLine(invoice.getNumFactura());

            for (LineaFactura line : linesList) {
                data.addCell( new Cell().add(String.valueOf(line.getNumNeu())));
                data.addCell(new Cell().add(line.getConcept()));
                data.addCell(new Cell().add(String.valueOf(line.getPrice())));
            }

            doc.add(data);
            //Añadimos salto de línea
            doc.add(new Paragraph("\n"));

            //Añadimos los totales
            Table invoiceTotals = new Table(new float[]{2, 2});
            invoiceTotals.setWidthPercent(45);
            invoiceTotals.setMarginLeft(200);
            invoiceTotals.addCell(new Cell().add("Base imponible: ")).setTextAlignment(TextAlignment.LEFT);
            invoiceTotals.addCell(new Cell().add(String.valueOf(invoice.getBase())));
            invoiceTotals.addCell(new Cell().add("IVA: ")).setTextAlignment(TextAlignment.LEFT);
            invoiceTotals.addCell(new Cell().add(String.valueOf(invoice.getTaxAmount())));
            invoiceTotals.addCell(new Cell().add("Total Factura: ")).setTextAlignment(TextAlignment.LEFT).setBold();
            invoiceTotals.addCell(new Cell().add(String.valueOf(invoice.getTotal()))).setBold();
            //Recorremos la tabla para quitarle los bordes a las celdas
            for (IElement iElement : invoiceTotals.getChildren()) {
                ((Cell) iElement).setBorder(Border.NO_BORDER);
            }
            //Añadimos la tabla con los totales de la factura al documento
            doc.add(invoiceTotals);
            //Añadimos salto de línea
            doc.add(new Paragraph("\n\n\n"));

            //Añadimos el número de cuenta
            doc.add(new Paragraph("Forma de pago: Transferencia\nNº Cuenta: " + invoice.getBankAccount()).setBold());
            
            doc.close();

            JOptionPane.showMessageDialog(null, "Se ha creado el fichero " + invoceName + " con la factura correspondiente");

        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        }
    }

