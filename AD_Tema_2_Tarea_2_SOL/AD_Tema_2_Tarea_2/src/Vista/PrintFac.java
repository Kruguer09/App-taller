    public void initTable() {
        //Inicializamos modelo
        model = new DefaultTableModel();
        //Nombramos las columnas
        String[] columnNames = {"Nº Factura", "Fecha", "NIF Cliente", "NIF Emisor", "Subtotal", "IVA", "Total"};
        model.setColumnIdentifiers(columnNames);
        model.setRowCount(0);

        //Accedemos a los datos
        invoicesList = con.readTableInv();
        if (invoicesList.size() != 0) {
            for (Factura inv : invoicesList) {
                Object[] rowData = new Object[columnNames.length];
                rowData[0] = inv.getNumFactura();
                rowData[1] = inv.getCurrentDate();
                rowData[2] = inv.getNifClient();
                rowData[3] = inv.getNifCompany();
                rowData[4] = inv.getBase();
                rowData[5] = inv.getTaxAmount();
                rowData[6] = inv.getTotal();
                model.addRow(rowData);
            }
        }

        this.jTInvList.setModel(model);
    }

